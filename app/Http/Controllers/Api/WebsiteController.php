    public function index()
    {
        #get data
        $data['sites'] = Website::oldest('url')->get();

        $data['sites']->transform(function ($row, $key) {
            $row->url = explode(".", $row->url)[0];

            return $row;
        });

        #return json response
        return response()->json($data);
    }

